package modelos;

public class Nota {
	private double nexamen;
	private double ndeberes;
	private double nlecciones;
	private double ntalleres;
	
	public Nota() {
		this.nexamen = 0;
		this.ndeberes = 0;
		this.nlecciones = 0;
		this.ntalleres = 0;
	}
	
	public double getNexamen() {
		return nexamen;
	}
	
	public double getNdeberes() {
		return ndeberes;
	}
	
	public double getNlecciones() {
		return nlecciones;
	}
	
	public double getNtalleres() {
		return ntalleres;
	}
	
	
}
